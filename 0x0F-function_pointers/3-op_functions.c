#include "3-calc.h"
/**
 * op_add - function to get sum of two number
 * @a: the first number
 * @b: the second number
 * Return: summation of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function to get subtractor of two number
 * @a: the first number
 * @b: the second number
 * Return: sub of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - function to get multiplication of two number
 * @a: the first number
 * @b: the second number
 * Return: mul of two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - function to get division of two number
 * @a: the first number
 * @b: the second number
 * Return: division of two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - function to get moduls of two number
 * @a: the first number
 * @b: the second number
 * Return: mod of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
